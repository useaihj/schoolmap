def parse_raw_data(raw_text, default_type=None):
    """원시 파이프 구분 텍스트를 파싱"""
    schools = []
    for line in raw_text.strip().split("\n"):
        line = line.strip()
        if not line:
            continue
        parts = line.split("|")
        if len(parts) < 3:
            continue

        name = parts[0].strip()
        lat_str = parts[1].strip()
        lon_str = parts[2].strip()

        # 제외 항목 스킵
        if lat_str == "제외" or lon_str == "제외":
            continue

        try:
            lat = float(lat_str)
            lon = float(lon_str)
        except ValueError:
            continue

        # 폐교, 예정, 운동장 등 제외
        if any(kw in name for kw in ["폐교", "예정", "운동장"]):
            continue

        # 학교급 분류
        if default_type:
            school_type = default_type
        else:
            school_type = classify_school(name)

        address = parts[3].strip() if len(parts) > 3 else ""

        schools.append({
            "name": clean_name(name),
            "type": school_type,
            "lat": round(lat, 7),
            "lng": round(lon, 7),
            "address": address,
        })

    return schools


def classify_school(name):
    """학교 이름으로 학교급 분류"""
    if "초등학교" in name:
        return "초등학교"
    elif "중학교" in name:
        return "중학교"
    elif "고등학교" in name:
        return "고등학교"
    elif "특수학교" in name or "학교" in name:
        return "기타"
    return "기타"


def clean_name(name):
    """학교명 정제"""
    # 병설유치원 등 부속 정보 제거
    name = re.split(r"[·]", name)[0].strip()
    # 앞뒤 공백 제거
    return name.strip()


def get_district(lat, lng):
    """위경도 기반 울산 구/군 추정 (개선된 경계)"""
    # 동구: 동해안 쪽, 경도 129.39 이상 & 위도 35.46~35.57
    if lng >= 129.39 and 35.46 <= lat <= 35.57:
        return "동구"

    # 북구: 위도 35.59 이상 & 경도 129.30~129.45 (도심 북부)
    if lat >= 35.59 and 129.30 <= lng <= 129.45:
        return "북구"

    # 중구: 울산 중심부 (위도 35.55~35.59, 경도 129.29~129.37)
    if 35.545 <= lat < 35.59 and 129.285 <= lng <= 129.37:
        return "중구"

    # 남구: 중심부 남쪽 (위도 35.50~35.555, 경도 129.26~129.35)
    if 35.50 <= lat < 35.555 and 129.26 <= lng <= 129.35:
        return "남구"

    # 남구: 삼산/무거/옥동 지역 보정
    if 35.525 <= lat < 35.56 and 129.24 <= lng < 129.30:
        return "남구"

    # 울주군: 나머지 외곽 지역
    return "울주군"


def deduplicate(schools):
    """이름 기반 중복 제거 (좌표가 더 정확한 것 우선)"""
    seen = {}
    for s in schools:
        key = s["name"]
        if key in seen:
            # 주소가 있는 쪽 우선
            if s["address"] and not seen[key]["address"]:
                seen[key] = s
        else:
            seen[key] = s
    return list(seen.values())


def main():
    # 1) 원시 데이터 파싱
    elementary = parse_raw_data(RAW_ELEMENTARY, default_type="초등학교")
    middle_high = parse_raw_data(RAW_MIDDLE_HIGH)

    # 2) 합치기
    all_schools = elementary + middle_high

    # 3) 중복 제거
    all_schools = deduplicate(all_schools)

    # 4) 구/군 추정 및 정보 보강
    for s in all_schools:
        if not s.get("district"):
            s["district"] = get_district(s["lat"], s["lng"])

    # 5) 이름순 정렬
    all_schools.sort(key=lambda x: (x["type"], x["name"]))

    # 6) ID 부여
    for i, s in enumerate(all_schools, 1):
        s["id"] = i

    # 통계
    types = {}
    districts = {}
    for s in all_schools:
        types[s["type"]] = types.get(s["type"], 0) + 1
        districts[s["district"]] = districts.get(s["district"], 0) + 1

    print(f"=== 울산광역시 학교 데이터 가공 완료 ===")
    print(f"총 학교 수: {len(all_schools)}")
    print(f"\n학교급별:")
    for t, c in sorted(types.items()):
        print(f"  {t}: {c}개")
    print(f"\n구/군별:")
    for d, c in sorted(districts.items()):
        print(f"  {d}: {c}개")

    # 7) JSON 저장
    output_path = Path(__file__).parent.parent / "data" / "ulsan_schools.json"
    dataset = {
        "metadata": {
            "title": "울산광역시 학교 위치 데이터",
            "source": "OpenStreetMap Overpass API + NEIS 교육정보",
            "coordinate_system": "WGS84 (EPSG:4326)",
            "last_updated": "2026-04-03",
            "total_count": len(all_schools),
            "types": types,
            "districts": districts,
        },
        "schools": all_schools,
    }

    with open(output_path, "w", encoding="utf-8") as f:
        json.dump(dataset, f, ensure_ascii=False, indent=2)

    print(f"\n저장 완료: {output_path}")
    return dataset


